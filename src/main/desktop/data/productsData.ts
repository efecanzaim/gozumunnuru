// Ürün listesini sıralama fonksiyonu
export function sortProducts(products: Product[], orderOption: string): Product[] {
  const sortedProducts = [...products];
  
  switch (orderOption) {
    case 'artanfiyat':
      return sortedProducts.sort((a, b) => (a.actualPrice || a.price) - (b.actualPrice || b.price));
    case 'azalanfiyat':
      return sortedProducts.sort((a, b) => (b.actualPrice || b.price) - (a.actualPrice || a.price));
    case 'indirimliurunler':
      return sortedProducts.filter(p => p.hasDiscount);
    case 'yeniurunler':
    case 'akillisiralama':
    case 'editor':
    default:
      return sortedProducts;
  }
}

